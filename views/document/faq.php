    <div class="subheading">
        What is qhojo?
    </div>
    <div class="subcontent">
        qhojo is a peer-to-peer renting service for small goods. Lenders can post their item for rent by suppling some item details, a daily rental rate, and a deposit value. Borrowers can reserve these items for a duration, meet the lender, and borrow away.
    </div>
