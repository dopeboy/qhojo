    <div class="subcontent">
        In the fall of 2012, I was planning my first trip to Turkey. I wanted to explore Istanbul as well as the countryside. 
        I knew I needed a traveling backpack for this itinerary so I went to a local sports goods store and bought a $300 20 liter traveling backpack. The trip and backpack were great. But ever since then,
        my backpack has been in my closet.
 
        Sitting unused. Collecting dust. And wasting space.
        <br/><br/>
        Cleaning out my closet one day, I had two thoughts:
        <br/>
        <ol>
            <li>Wouldn't it be cool if I could have rented the backpack out to begin with and saved some money?<br/><br/>
            <li>Wouldn't it be cool if I could rent this backpack out right now and make some money?
        </ol>
        I couldn't find any comparable service out there that would let me rent and borrow items from other people. <br/><br/>
        So I built one.
        <br/><br/>
        Launched in the summer of 2013, <b>qhojo</b> (pronounced co-joe) is a peer-to-peer rental service for small goods.
        <br/><br/>
        <i>-Manish Sinha, Founder</i>
